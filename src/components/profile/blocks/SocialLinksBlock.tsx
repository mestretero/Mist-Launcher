export function SocialLinksBlock({ config, isEditing, onConfigChange }: BlockProps) {
  const { t } = useTranslation();
  const links: SocialLink[] = config.links ?? [];

  function updateLink(index: number, field: keyof SocialLink, value: string) {
    const updated = links.map((link, i) =>
      i === index ? { ...link, [field]: value } : link
    );
    onConfigChange({ ...config, links: updated });
  }

  function addLink() {
    onConfigChange({ ...config, links: [...links, { platform: "discord", url: "" }] });
  }

  function removeLink(index: number) {
    onConfigChange({ ...config, links: links.filter((_, i) => i !== index) });
  }

  if (isEditing) {
    return (
      <div className="space-y-3">
        {links.map((link, i) => (
          <div key={i} className="flex items-center gap-2">
            <select
              value={link.platform}
              onChange={(e) => updateLink(i, "platform", e.target.value)}
              className="px-2 py-2 rounded-lg bg-[#0a0c10] border border-[#2a2e38] text-white text-sm focus:outline-none focus:border-[#1a9fff] transition-colors"
            >
              {PLATFORMS.map((p) => (
                <option key={p} value={p}>
                  {p.charAt(0).toUpperCase() + p.slice(1)}
                </option>
              ))}
            </select>
            <input
              type="url"
              value={link.url}
              onChange={(e) => updateLink(i, "url", e.target.value)}
              placeholder={t("profile.blocks.socialUrlPlaceholder", "https://...")}
              className="flex-1 px-3 py-2 rounded-lg bg-[#0a0c10] border border-[#2a2e38] text-white text-sm placeholder-[#5e6673] focus:outline-none focus:border-[#1a9fff] transition-colors"
            />
            <button
              onClick={() => removeLink(i)}
              className="p-2 rounded-lg bg-[#0a0c10] border border-[#2a2e38] text-[#8f98a0] hover:text-red-400 hover:border-red-900/50 transition-colors flex-shrink-0"
            >
              <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                <path d="M18 6L6 18M6 6l12 12" />
              </svg>
            </button>
          </div>
        ))}
        <button
          onClick={addLink}
          className="w-full px-3 py-2 rounded-lg border border-dashed border-[#2a2e38] text-[#5e6673] text-sm hover:border-[#1a9fff] hover:text-[#1a9fff] transition-colors"
        >
          + {t("profile.blocks.socialAddLink", "Add Link")}
        </button>
      </div>
    );
  }

  if (links.length === 0 || links.every((l) => !l.url)) {
    return (
      <p className="text-sm text-[#5e6673] italic">
        {t("profile.blocks.socialEmpty", "No social links added.")}
      </p>
    );
  }

  return (
    <div className="flex flex-wrap gap-3">
      {links
        .filter((l) => l.url)
        .map((link, i) => (
          <a
            key={i}
            href={link.url}
            target="_blank"
            rel="noopener noreferrer"
            title={link.platform}
            className="flex items-center gap-2 px-4 py-2.5 rounded-xl border border-[#2a2e38] hover:border-[#1a9fff]/40 transition-all hover:scale-105 bg-[#0a0c10]"
            style={{ color: PLATFORM_COLORS[link.platform as Platform] ?? "#c6d4df" }}
          >
            <PlatformIcon platform={link.platform} />
            <span className="text-sm font-semibold text-white capitalize">{link.platform}</span>
          </a>
        ))}
    </div>
  );
}
